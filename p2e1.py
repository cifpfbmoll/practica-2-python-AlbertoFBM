# Diagrama para calcular y mostrar el área de un triángulo.

print ("Hola bienvenido a la calculadora de áreas de triángulos, por favor \n\
ponga los datos correspondientes:")
print ("- Ponga el valor de la Base:")
base = float(input())
print ("La base que has puesto es:", base)
print ("- Ponga el valor de la altura")
altura = float(input())
print("La altura que has puesto es:",altura)
area = float((base * altura) / 2)
print ("El area de su triangulo es:", area)